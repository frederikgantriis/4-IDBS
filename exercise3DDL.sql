CREATE TABLE Projects (
    ID INT NOT NULL,
    MN VARCHAR(50) NOT NULL,
    PRIMARY KEY(ID)
);

CREATE TABLE MN (
    MN VARCHAR(50) NOT NULL,
    MID INT NOT NULL,
    FOREIGN KEY (MN)
);

CREATE TABLE SID (
    SID INT NOT NULL, 
    SN VARCHAR(50) NOT NULL, 
    PRIMARY KEY (SID)
);

CREATE TABLE PID (
    PID INT NOT NULL, 
    PN VARCHAR(50) NOT NULL, 
    PRIMARY KEY (PID)
);


